package com.beans;

public class APour {

	int idRole;
	int idProf;
	
	public APour() {
		super();
	}
	
	public APour(int idRole, int idProf) {
		super();
		this.idRole = idRole;
		this.idProf = idProf;
	}

	public int getIdRole() {
		return idRole;
	}
	public void setIdRole(int idRole) {
		this.idRole = idRole;
	}
	public int getIdProf() {
		return idProf;
	}
	public void setIdProf(int idProf) {
		this.idProf = idProf;
	}
	
	
}
